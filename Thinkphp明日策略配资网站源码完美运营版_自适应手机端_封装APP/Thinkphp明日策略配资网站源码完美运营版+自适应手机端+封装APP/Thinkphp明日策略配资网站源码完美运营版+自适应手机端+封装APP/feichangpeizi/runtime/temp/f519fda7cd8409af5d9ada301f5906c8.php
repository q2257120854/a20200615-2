        <p>本次交易合作由金龙策略投资中介平台（以下简称“金龙策略投资”）子平台金龙策略（以下简称“金龙策略”）提供撮合服务。在金龙策略的撮合下，投资人与策略人协商一致，就双方秉承共享收益共担风险之原则，由策略人提供交易策略，由投资人利用自有资金和账户进行交易合作事宜订立如下协议，以兹共守：</p>
        <h3 class="topic">一、定义</h3>
        <p>本协议中，除非上下文另有解释，下列词语具有如下含义：</p>
        <p>1、交易平台：指金龙策略投资中介平台，由宁波鑫钰网络科技有限公司运营。</p>
        <p>2、金龙策略：指金龙策略信息平台，面向金龙策略注册用户提供包括但不限于沪深所A股交易合作信息发布、交易合作撮合、交易指令通讯、交易风控管理、交易代记账等服务的线上中介服务平台。</p>
        <p>3、支付机构：指金龙策略委托的为金龙策略用户提供资金划转、查询、结算等支付服务的非金融支付机构。本协议项下提供上述服务的机构为连连网络科技有限公司（以下简称“连连”）。</p>
        <p>4、策略人：指通过金龙策略注册成为策略人的用户，作为投资人的交易合作方，负责向投资人提供交易策略的自然人。</p>
        <p>5、投资人：指通过金龙策略注册成为投资人的用户，作为策略人的交易合作方，负责按策略人交易策略并利用自有资金和账户进行交易的自然人或法人。</p>
        <p>6、点买：指策略人向投资人发出买入指令。策略指令为即时买入。即时买入指策略人要求投资人在收到交易指令所在时点起，即时以卖五价委托买入指定数量的交易品种。</p>
        <p>7、点卖：指策略人向投资人发出卖出指令。点卖指令为即时卖出。即时卖出为策略人要求投资人在收到交易指令所在时点起，即时以买五价委托卖出全部数量的交易品种。</p>
        <p>8、持仓时间：分为T＋1持仓与T＋1｜D持仓。T＋1持仓，交易品种持仓时间固定截至T＋1日；T＋1｜D持仓，从T＋1日开始，至T＋20日止，策略人在当日符合延递条件情况下，可于当日规定时间内，向投资人 申请将交易品种持仓时间延递至下一交易日。延递按日申请逐日延递，单笔交易连续延递最大次数为20次（含系统自动延递）。</p>
        <p>9、最后持仓日：T＋1持仓的最后持仓日即为T＋1日，T＋1｜D持仓的最后持仓日为不符合延递条件所在交易日，或符合延递条件但策略人放弃延递所在交易日，或已达到最大延递申请次数无法再延递的所在交易日即T＋21日。</p>
        <p>10、浮动盈亏与浮动盈亏率：浮动盈亏＝（最新价－买入均价）＊成交数量。计算结果为正值（含零）时即为浮盈金额，计算结果为负值时即为浮亏金额。浮动盈亏率＝浮动盈亏／交易本金 ×100％。</p>
        <p>11、触发止盈率：当合作交易品种的浮动盈亏率达到特定数值时，由投资人即时卖出交易品种全部持有数量进行止盈，此时该浮动盈亏率即为触发止盈率。无论交易品种的持仓时间为T+1还是T＋1｜D，固定触发止盈率为：实盘金额*50%，其他盈利点位，策略人可根据实际情况自行点卖进行卖出止盈。</p>
