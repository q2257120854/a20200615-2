        <h3 class="topic">三、投资人权利与义务</h3>
        <p>1、投资人负责提供用于本次交易合作的账户和资金。</p>
        <p>2、投资人按照策略人的策略指令/点卖指令执行交易品种的买入/卖出。</p>
        <p>3、投资人买入的时间为T日上午9:30－11:30，下午13:00 – 14:55</p>
        <p>4、投资人卖出的时间为最后持仓日上午9:30－11:30，下午13:00 – 14:40</p>
        <p>5、最后持仓日盘中，当交易品种“浮盈金额≥触发止盈时”或”浮亏金额≤触发止损时”，视同策略人已向投资人发出即时卖出指令，投资人执行即时卖出指令的，策略人对此不持有异议。</p>
        <p>6、策略人必须在最后持仓日14:40前向投资人发出卖出指令，否则视同策略人违约，投资人有权在此后任意交易时间卖出并以实际卖出成交价结算，策略人对此不持有异议。</p>
        <p>7、交易品种T日盘中涨幅≥8％或盘中跌幅≤-8％时，投资人均有权不接受并不执行策略人的策略指令，直至交易品种涨跌幅回落到（-8％,+8%）区间，投资人方接受并执行策略人的策略指令，但当交易品种被金龙策略列入《策略风险警示股名单》中，投资人有权不接受策略人的策略指令。</p>
        <p>8、T＋1｜D持仓，从T＋1日开始，至T＋20日止，当日14:40:00策略满足浮动盈亏/履约保证金≥-60%（数据仅供参考，以平台实际最新公布的为准），投资人应接受策略人的延递申请,但当交易品种被金龙策略列入《延递风险警示股名单》中，投资人有权不接受策略人的延递申请。如T＋1日至T＋30日策略人股票全天停牌，则符合上述延递条件下，投资人自动为策略人延递，且该次延递收取延递费。如策略人不愿意支付延递费，视为策略人放弃该股票复盘后的收益，全归投资人所有。</p>
        <p>9、投资人应通过金龙策略账户委托金龙策略冻结履约保证金，并授权连连系统在交易盈利时按本协议相关约定，从履约保证金中支付策略人盈利分配款。投资人履约保证金金额应不低于 “止盈值”之数额。</p>
        <p>10、交易亏损大于策略人冻结履约保证金，则超过的亏损部分由投资人自行承担。</p>
        <p>11、投资人有义务履行交易合作约定，如发生投资人单方面违约行为，双方约定按理论上持有交易品种到最后持仓日期间的最优价格作为策略人卖出结算价，如交易盈利从投资人金龙策略账户划拨盈利分配，如交易亏损则由投资人自行全部承担，策略人不予赔付。</p>
       	<h3 class="topic">四、策略人权利与义务</h3>
        <p>1、策略人负责提供本次交易合作的交易策略，即策略指令／点卖指令。</p>
        <p>2、策略人应在T日9:30－14:58交易时段内向投资人发出策略指令，但当交易品种T日盘中涨幅≥8％或盘中跌幅≤-8％时，或交易品种被金龙策略列入《策略风险警示股名单》中，策略人不可向投资人申请策略 。</p>
        <p>3、策略人应在最后持仓日9:30－14:40向投资人发出点卖指令，否则视同策略人违约。</p>
        <p>4、T＋1｜D 持仓，从T＋1日开始，至T＋20日止，当日14:40策略满足浮动盈亏/履约保证金≥-60%（数据仅供参考，以平台实际最新公布的为准），策略人可通过金龙策略向投资人 申请将交易品种持仓时间延递至下一交易日14:40。但当交易品种被金龙策略平台列入《延递风险警示股名单》中，策略人无法延递。</p>
        <p>5、策略人应按照《附件1：”金龙策略”策略人参与沪深A股交易涉及费用及资费标准》，通过向金龙策略支付交易综合费，补偿投资人交易过程中发生的佣金、印花税和过户费等</p>
        <p>6、策略人应按照《附件1：”金龙策略”策略人参与沪深A股交易涉及费用及资费标准》，委托金龙策略冻结履约保证金，作为履行交易亏损赔付义务的保证。</p>
        <p>7、策略人承诺授权金龙策略平台在交易亏损时按本协议相关约定，从履约保证金中向投资人支付应承担的亏损赔付款。</p>
        <h3 class="topic">五、交易清结算和盈亏分配约定</h3>
        <p>投资人和策略人共同委托金龙策略按照以下规则对本次交易合作进行清结算，双方同意以金龙策略的清结算数据作为双方盈亏分配的共同依据：</p>
        <p>1、买入成交金额与买入成交均价：买入成交金额来自交易所回报数据，不含佣金、印花税和过户费。买入成交均价＝买入成交金额／交易数量。</p>
        <p>2、卖出成交金额与卖出成交均价：卖出成交金额来自交易所回报数据，不含佣金、印花税和过户费。卖出成交均价＝卖出成交金额／交易数量。</p>
        <p>3、交易盈亏：交易盈亏＝卖出成交金额－买入成交金额。计算结果为正值（含零）时即为交易盈利，计算结果为负值时即为交易亏损 。</p>
        <p>4、盈利分配：策略人与投资人共同享有本次交易盈利。策略人获得交易盈利100％的盈利分配。</p>
        <p>5、亏损赔付：策略人与投资人共同承担本次交易亏损。交易亏损首先由策略人承担，策略人以所冻结的履约保证金为最高上限承担交易亏损赔付，交易亏损超过策略人所冻结的履约保证金部分由投资人承担。即，当：交易亏损≤策略人冻结履约保证金时，交易亏损由策略人全部承担，投资人无需承担；当：交易亏损＞策略人冻结履约保证金时，策略人以冻结履约保证金额为限承担交易亏损，超过部分由投资人承担。</p>
        <h3 class="topic">六、特别约定</h3>
        <p>1、双方约定为从投资人收到策略人交易指令时点起,至投资人委托下单成交时点止，两时点间隔不超过60秒即为即时买入／即时卖出的时间范畴。根据《金龙策略服务协议》，从策略人发出交易指令被金龙策略确认时点起，至交易指令成交时点止，两时点时间间隔不超过60秒。但：</br>
        	&nbsp;&nbsp;1）深市收盘集合竞成交价为买卖申报一次性集中撮合产生，投资人收到策略人交易指令时点在 14:55之后的，不属于以上时间范畴。</br>
        	&nbsp;&nbsp;2）如因网络等原因造成交易成交结果回报时间延迟的，以实际交易成交时间为准。</br>
        	&nbsp;&nbsp;3）投资人收到策略人交易指令在11:25~11:30之间的，投资人可在当日13:00~13:05委托下单
        </p>
        <p>2、交易品种在最后持仓日因停牌、跌停或其他原因导致无法卖出的，双方约定由投资人于下一交易日集合竞价和连续竞价前5分钟内即时卖出，并保持卖出委托直至卖出全部数量的交易品种，如整个交易日皆因停牌或跌停无法卖出，则继续顺延至下一交易日，直至全部卖出并按照实际卖出成交价进行清结算。</p>
        <p>3、T＋1｜D持仓且非最后持仓日，为最大程度避免交易品种因停牌导致无法正常卖出，双方约定如交易品种当日进入金龙策略实时发布的《延递风险警示股名单》。</p>
        <p>4、策略人在分红转增股权登记日收盘后仍持有交易品种的，双方约定于送转股票及派发红利实际到达投资人账户后方进行结算；策略人在增发、配股股权登记日收盘后未持有交易品种的，交易品种的增发、配股等权利均归属投资人，策略人不享有参与增发、配股权利。</p>
        <p>5、双方授权连连系统依据金龙策略的清结算数据，通过双方在金龙策略系统冻结的履约保证金进行盈利分配款或亏损赔付款的划拨。即：交易盈利时，从投资人履约保证金中划款至策略人账户，划拨金额等于策略人享有的盈利分配额；交易亏损时，从策略人履约保证金中划款至投资人账户，划拨金额等于策略人承担的亏损赔付额。</p>
        <p>6、因金龙策略清结算数据错误造成双方盈利分配或亏损赔付出现数额差错的，双方约定在金龙策略规定时限内将异议提交至金龙策略，通过金龙策略向对方索取差额补差。如异议一方未在规定时限内将提交异议，视同放弃通过金龙策略向对方索取差额补差之权利。</p>
        <p>7、因不可抗力导致金龙策略系统故障，引发交易指令通讯失败或交易指令执行失败，双方约定以金龙策略系统恢复就近时间重新执行并以执行的最终结果进行交易清结算。</p>
